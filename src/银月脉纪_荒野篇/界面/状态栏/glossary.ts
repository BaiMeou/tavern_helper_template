// ── 全局事件桥（GlossaryOverlay 监听） ──
const EVT = '__wilderness_glossary_open__';
type Payload = { terms: string[]; title?: string };

export function openGlossary(scopeOrTerms: string | string[]) {
  const terms = Array.isArray(scopeOrTerms) ? scopeOrTerms : (PAGE_TERMS[scopeOrTerms] || []);
  const title = Array.isArray(scopeOrTerms) ? '术语说明' : '本页术语说明';
  window.dispatchEvent(new CustomEvent<Payload>(EVT, { detail: { terms, title } }));
}
export function openTerm(term: string) { openGlossary([term]); }

export const GLOSSARY_EVENT = EVT;
export type { Payload as GlossaryPayload };
